#include <bits/stdc++.h>

using namespace std;

int main()

{

    ios_base::sync_with_stdio(0);

    cin.tie(0);

    cout.tie(0);

    int t;

    cin >> t;

    for (int i = 0; i < t; i++)

    {

        int n;

        cin >> n;

        cout << n << endl;

        for (int j = 0; j < n; j++) cout << j + 1 << ' ';

        cout << endl;

    }

    return 0;

}