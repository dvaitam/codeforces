type testCase struct {
	t     int
	pairs [][2]int64
}

func solvePair(l, r int64) string {
	if 2*l > r {
		return "YES"
	}
	return "NO"
}

func parseTestcases() ([]testCase, error) {
	lines := strings.Split(testcasesRaw, "\n")
	var cases []testCase
	for idx, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		fields := strings.Fields(line)
		t, err := strconv.Atoi(fields[0])
		if err != nil {
			return nil, fmt.Errorf("line %d: invalid t: %v", idx+1, err)
		}
		if len(fields) != 1+2*t {
			return nil, fmt.Errorf("line %d: expected %d numbers got %d", idx+1, 1+2*t, len(fields))
		}
		pairs := make([][2]int64, t)
		pos := 1
		for i := 0; i < t; i++ {
			l, err1 := strconv.ParseInt(fields[pos], 10, 64)
			r, err2 := strconv.ParseInt(fields[pos+1], 10, 64)
			if err1 != nil || err2 != nil {
				return nil, fmt.Errorf("line %d: parse pair %d: %v %v", idx+1, i+1, err1, err2)
			}
			pairs[i] = [2]int64{l, r}
			pos += 2
		}
		cases = append(cases, testCase{t: t, pairs: pairs})
	}
	if len(cases) == 0 {
		return nil, fmt.Errorf("no embedded testcases")
	}
	return cases, nil
}

func buildIfGo(path string) (string, func(), error) {
	if strings.HasSuffix(path, ".go") {
		tmp, err := os.CreateTemp("", "solbin*")
		if err != nil {
			return "", nil, err
		}
		tmp.Close()
		out, err := exec.Command("go", "build", "-o", tmp.Name(), path).CombinedOutput()
		if err != nil {
			os.Remove(tmp.Name())
			return "", nil, fmt.Errorf("build failed: %v\n%s", err, out)
		}
		return tmp.Name(), func() { os.Remove(tmp.Name()) }, nil
	}
	return path, func() {}, nil
}

func runCandidate(bin, input string) (string, error) {
	cmd := exec.Command(bin)
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &out
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, out.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}

	cases, err := parseTestcases()
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}

	bin, cleanup, err := buildIfGo(os.Args[1])
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	defer cleanup()

	for i, tc := range cases {
		var input strings.Builder
		fmt.Fprintf(&input, "%d\n", tc.t)
		for _, p := range tc.pairs {
			fmt.Fprintf(&input, "%d %d\n", p[0], p[1])
		}

		got, err := runCandidate(bin, input.String())
		if err != nil {
			fmt.Printf("test %d: %v\n", i+1, err)
			os.Exit(1)
		}

		var expected strings.Builder
		for idx, p := range tc.pairs {
			if idx > 0 {
				expected.WriteByte('\n')
			}
			expected.WriteString(solvePair(p[0], p[1]))
		}

		if strings.TrimSpace(got) != strings.TrimSpace(expected.String()) {
			fmt.Printf("test %d failed\nexpected:\n%s\n\ngot:\n%s\n", i+1, expected.String(), got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(cases))
}
