func min(a, b int) int {
    if a < b {
        return a
    }
    return b
}

// Embedded solver logic from 1291B.go.
func solve(tc testcase) string {
    n := tc.n
    a := tc.arr
    prefix := make([]bool, n)
    ok := true
    for i := 0; i < n; i++ {
        if ok && a[i] >= int64(i) {
            prefix[i] = true
        } else {
            ok = false
        }
    }
    suffix := make([]bool, n)
    ok = true
    for i := n - 1; i >= 0; i-- {
        if ok && a[i] >= int64(n-1-i) {
            suffix[i] = true
        } else {
            ok = false
        }
    }
    for i := 0; i < n; i++ {
        if prefix[i] && suffix[i] {
            return "Yes"
        }
    }
    return "No"
}

func runCandidate(bin, input string) (string, error) {
    cmd := exec.Command(bin)
    cmd.Stdin = strings.NewReader(input)
    var out bytes.Buffer
    cmd.Stdout = &out
    cmd.Stderr = &out
    if err := cmd.Run(); err != nil {
        return "", fmt.Errorf("runtime error: %v\n%s", err, out.String())
    }
    return out.String(), nil
}

func checkCase(bin string, idx int, tc testcase) error {
    var sb strings.Builder
    sb.WriteString("1\n")
    sb.WriteString(fmt.Sprintf("%d\n", tc.n))
    for i, v := range tc.arr {
        if i > 0 {
            sb.WriteByte(' ')
        }
        sb.WriteString(strconv.FormatInt(v, 10))
    }
    sb.WriteByte('\n')
    input := sb.String()

    expected := strings.ToLower(solve(tc))
    out, err := runCandidate(bin, input)
    if err != nil {
        return err
    }
    got := strings.ToLower(strings.TrimSpace(out))
    if got != expected {
        return fmt.Errorf("case %d: expected %s got %s", idx+1, expected, got)
    }
    return nil
}

