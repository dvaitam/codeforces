type testCase struct {
	a int64
	b int64
	c int64
	d int64
}

func runCandidate(bin, input string) (string, error) {
	var cmd *exec.Cmd
	if strings.HasSuffix(bin, ".go") {
		cmd = exec.Command("go", "run", bin)
	} else {
		cmd = exec.Command(bin)
	}
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var errBuf bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errBuf
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, errBuf.String())
	}
	return out.String(), nil
}

func isTriangle(x, y, z int64) bool {
	return x+y > z && x+z > y && y+z > x
}

func parseTests() ([]testCase, error) {
	toks := strings.Fields(testcasesRaw)
	var tests []testCase
	for i := 0; i < len(toks); {
		if i+4 > len(toks) {
			return nil, fmt.Errorf("truncated test cases")
		}
		a, _ := strconv.ParseInt(toks[i], 10, 64)
		b, _ := strconv.ParseInt(toks[i+1], 10, 64)
		c, _ := strconv.ParseInt(toks[i+2], 10, 64)
		d, _ := strconv.ParseInt(toks[i+3], 10, 64)
		tests = append(tests, testCase{a: a, b: b, c: c, d: d})
		i += 4
	}
	return tests, nil
}

func expected(tc testCase) string {
	return fmt.Sprintf("%d %d %d", tc.a, tc.c, tc.c)
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]
	tests, err := parseTests()
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to parse testcases: %v\n", err)
		os.Exit(1)
	}
	for idx, tc := range tests {
		input := fmt.Sprintf("1\n%d %d %d %d\n", tc.a, tc.b, tc.c, tc.d)
		want := expected(tc)
		outStr, err := runCandidate(bin, input)
		if err != nil {
			fmt.Printf("test %d: %v\n", idx+1, err)
			os.Exit(1)
		}
		fields := strings.Fields(strings.TrimSpace(outStr))
		if len(fields) != 3 {
			fmt.Printf("test %d: expected three integers got %q\n", idx+1, outStr)
			os.Exit(1)
		}
		x, err1 := strconv.ParseInt(fields[0], 10, 64)
		y, err2 := strconv.ParseInt(fields[1], 10, 64)
		z, err3 := strconv.ParseInt(fields[2], 10, 64)
		if err1 != nil || err2 != nil || err3 != nil {
			fmt.Printf("test %d: invalid integers in output %q\n", idx+1, outStr)
			os.Exit(1)
		}
		if fmt.Sprintf("%d %d %d", x, y, z) != want {
			fmt.Printf("test %d failed: expected %s got %s\n", idx+1, want, strings.TrimSpace(outStr))
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(tests))
}
