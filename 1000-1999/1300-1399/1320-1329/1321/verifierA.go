type testCase struct {
	n int
	r []int
	b []int
}

func runProg(prog, input string) (string, error) {
	var cmd *exec.Cmd
	if strings.HasSuffix(prog, ".go") {
		cmd = exec.Command("go", "run", prog)
	} else {
		cmd = exec.Command(prog)
	}
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var errBuf bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errBuf
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, errBuf.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func lineToInput(line string) (string, error) {
	fields := strings.Fields(line)
	if len(fields) < 1 {
		return "", fmt.Errorf("empty test line")
	}
	n, err := strconv.Atoi(fields[0])
	if err != nil {
		return "", err
	}
	if len(fields) != 1+2*n {
		return "", fmt.Errorf("expected %d numbers got %d", 1+2*n, len(fields))
	}
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("%d\n", n))
	idx := 1
	for i := 0; i < n; i++ {
		sb.WriteString(fields[idx])
		idx++
		if i+1 < n {
			sb.WriteByte(' ')
		}
	}
	sb.WriteByte('\n')
	for i := 0; i < n; i++ {
		sb.WriteString(fields[idx])
		idx++
		if i+1 < n {
			sb.WriteByte(' ')
		}
	}
	sb.WriteByte('\n')
	return sb.String(), nil
}

func parseTests() ([]testCase, error) {
	tokens := strings.Fields(testcasesRaw)
	var tests []testCase
	for idx := 0; idx < len(tokens); {
		n, err := strconv.Atoi(tokens[idx])
		if err != nil {
			return nil, fmt.Errorf("invalid n at token %d: %v", idx+1, err)
		}
		idx++
		if idx+2*n > len(tokens) {
			return nil, fmt.Errorf("truncated test at token %d", idx)
		}
		r := make([]int, n)
		for i := 0; i < n; i++ {
			r[i], _ = strconv.Atoi(tokens[idx+i])
		}
		idx += n
		b := make([]int, n)
		for i := 0; i < n; i++ {
			b[i], _ = strconv.Atoi(tokens[idx+i])
		}
		idx += n
		tests = append(tests, testCase{n: n, r: r, b: b})
	}
	return tests, nil
}

func expected(tc testCase) string {
	a := 0
	c := 0
	for i := 0; i < tc.n; i++ {
		if tc.r[i] == 1 && tc.b[i] == 0 {
			a++
		} else if tc.r[i] == 0 && tc.b[i] == 1 {
			c++
		}
	}
	if a == 0 {
		return "-1"
	}
	return strconv.Itoa(c/a + 1)
}

func main() {
	if len(os.Args) != 2 {
		fmt.Fprintln(os.Stderr, "usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]
	tests, err := parseTests()
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}

	for i, tc := range tests {
		var sb strings.Builder
		sb.WriteString(strconv.Itoa(tc.n))
		sb.WriteByte('\n')
		for idx, v := range tc.r {
			sb.WriteString(strconv.Itoa(v))
			if idx+1 < len(tc.r) {
				sb.WriteByte(' ')
			}
		}
		sb.WriteByte('\n')
		for idx, v := range tc.b {
			sb.WriteString(strconv.Itoa(v))
			if idx+1 < len(tc.b) {
				sb.WriteByte(' ')
			}
		}
		sb.WriteByte('\n')
		input := sb.String()
		want := expected(tc)
		got, err := runProg(bin, input)
		if err != nil {
			fmt.Fprintf(os.Stderr, "candidate runtime error on test %d: %v\n", i+1, err)
			os.Exit(1)
		}
		if strings.TrimSpace(got) != want {
			fmt.Printf("Test %d failed\nInput:\n%sExpected:\n%s\nGot:\n%s\n", i+1, input, want, got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(tests))
}
