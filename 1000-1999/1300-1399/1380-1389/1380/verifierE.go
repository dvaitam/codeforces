type testCaseE struct {
	n, m    int
	t       []int
	queries [][2]int
}

func parseTestcases() ([]testCaseE, error) {
	tokens := strings.Fields(testData)
	if len(tokens) == 0 {
		return nil, fmt.Errorf("no test data")
	}
	idx := 0
	T, err := strconv.Atoi(tokens[idx])
	if err != nil {
		return nil, fmt.Errorf("invalid test count")
	}
	idx++
	cases := make([]testCaseE, 0, T)
	for caseIdx := 0; caseIdx < T; caseIdx++ {
		if idx+1 >= len(tokens) {
			return nil, fmt.Errorf("unexpected end of data at case %d", caseIdx+1)
		}
		n, err := strconv.Atoi(tokens[idx])
		if err != nil {
			return nil, fmt.Errorf("invalid n at case %d", caseIdx+1)
		}
		m, err := strconv.Atoi(tokens[idx+1])
		if err != nil {
			return nil, fmt.Errorf("invalid m at case %d", caseIdx+1)
		}
		idx += 2
		if idx+n > len(tokens) {
			return nil, fmt.Errorf("not enough t values at case %d", caseIdx+1)
		}
		t := make([]int, n+1)
		for i := 1; i <= n; i++ {
			v, err := strconv.Atoi(tokens[idx])
			if err != nil {
				return nil, fmt.Errorf("invalid t value at case %d", caseIdx+1)
			}
			t[i] = v
			idx++
		}
		queries := make([][2]int, m-1)
		for j := 0; j < m-1; j++ {
			if idx+1 >= len(tokens) {
				return nil, fmt.Errorf("not enough queries at case %d", caseIdx+1)
			}
			a, err := strconv.Atoi(tokens[idx])
			if err != nil {
				return nil, fmt.Errorf("invalid query at case %d", caseIdx+1)
			}
			b, err := strconv.Atoi(tokens[idx+1])
			if err != nil {
				return nil, fmt.Errorf("invalid query at case %d", caseIdx+1)
			}
			queries[j] = [2]int{a, b}
			idx += 2
		}
		cases = append(cases, testCaseE{n: n, m: m, t: t, queries: queries})
	}
	return cases, nil
}

