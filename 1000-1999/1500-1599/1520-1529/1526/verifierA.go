func run(bin, input string) (string, error) {
	var cmd *exec.Cmd
	if strings.HasSuffix(bin, ".go") {
		cmd = exec.Command("go", "run", bin)
	} else {
		cmd = exec.Command(bin)
	}
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var stderr bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &stderr
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, stderr.String())
	}
	return strings.TrimSpace(out.String()), nil
}

type testCase struct {
	n   int
	arr []int
}

func parseTestcases(raw string) ([]testCase, error) {
	sc := bufio.NewScanner(strings.NewReader(strings.TrimSpace(raw)))
	sc.Buffer(make([]byte, 1024), 1<<20)
	lines := make([]string, 0)
	for sc.Scan() {
		line := strings.TrimSpace(sc.Text())
		if line == "" {
			continue
		}
		lines = append(lines, line)
	}
	if err := sc.Err(); err != nil {
		return nil, fmt.Errorf("failed scanning testcases: %w", err)
	}
	if len(lines) == 0 {
		return nil, fmt.Errorf("no testcases found")
	}
	t, err := strconv.Atoi(lines[0])
	if err != nil {
		return nil, fmt.Errorf("invalid test count: %w", err)
	}
	idx := 1
	tests := make([]testCase, 0, t)
	for i := 0; i < t; i++ {
		if idx >= len(lines) {
			return nil, fmt.Errorf("unexpected EOF before case %d n", i+1)
		}
		n, err := strconv.Atoi(lines[idx])
		if err != nil {
			return nil, fmt.Errorf("invalid n on case %d: %w", i+1, err)
		}
		idx++
		if idx >= len(lines) {
			return nil, fmt.Errorf("unexpected EOF before case %d array", i+1)
		}
		parts := strings.Fields(lines[idx])
		if len(parts) != 2*n {
			return nil, fmt.Errorf("case %d: expected %d numbers got %d", i+1, 2*n, len(parts))
		}
		idx++
		arr := make([]int, 2*n)
		for j, p := range parts {
			val, err := strconv.Atoi(p)
			if err != nil {
				return nil, fmt.Errorf("case %d: invalid number %q", i+1, p)
			}
			arr[j] = val
		}
		tests = append(tests, testCase{n: n, arr: arr})
	}
	return tests, nil
}

func solve(tc testCase) string {
	v := make([]int, len(tc.arr))
	copy(v, tc.arr)
	sort.Ints(v)
	var sb strings.Builder
	l, r := 0, len(v)-1
	first := true
	for l < r {
		if !first {
			sb.WriteByte(' ')
		}
		first = false
		sb.WriteString(fmt.Sprintf("%d %d", v[l], v[r]))
		l++
		r--
	}
	return sb.String()
}

func buildInput(tc testCase) string {
	var sb strings.Builder
	sb.WriteString("1\n")
	sb.WriteString(strconv.Itoa(tc.n))
	sb.WriteByte('\n')
	for i, v := range tc.arr {
		if i > 0 {
			sb.WriteByte(' ')
		}
		sb.WriteString(strconv.Itoa(v))
	}
	sb.WriteByte('\n')
	return sb.String()
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	candidate := os.Args[1]

	tests, err := parseTestcases(testcasesRaw)
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to parse testcases: %v\n", err)
		os.Exit(1)
	}

	for caseNum, tc := range tests {
		input := buildInput(tc)
		exp := solve(tc)
		out, err := run(candidate, input)
		if err != nil {
			fmt.Fprintf(os.Stderr, "case %d failed: %v\n", caseNum+1, err)
			os.Exit(1)
		}
		if out != exp {
			fmt.Fprintf(os.Stderr, "case %d failed: expected %s got %s\n", caseNum+1, exp, out)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(tests))
}
