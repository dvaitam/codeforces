type testCase struct {
	n   int
	arr []int
}

// solve mirrors the logic from 1145A.go for a single test case.
func solve(arr []int) int {
	n := len(arr)
	check := func(a []int, l, r int) bool {
		for i := l + 1; i <= r; i++ {
			if a[i] < a[i-1] {
				return false
			}
		}
		return true
	}
	var thanos func([]int, int, int) int
	thanos = func(a []int, l, r int) int {
		if l == r {
			return 1
		}
		if check(a, l, r) {
			return r - l + 1
		}
		m := (l + r) / 2
		left := thanos(a, l, m)
		right := thanos(a, m+1, r)
		if left > right {
			return left
		}
		return right
	}
	return thanos(arr, 0, n-1)
}

func parseTestCases(data string) ([]testCase, error) {
	lines := strings.Split(data, "\n")
	cases := []testCase{}
	for idx, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		parts := strings.Fields(line)
		n, err := strconv.Atoi(parts[0])
		if err != nil {
			return nil, fmt.Errorf("line %d: invalid n: %w", idx+1, err)
		}
		if len(parts) < n+1 {
			return nil, fmt.Errorf("line %d: expected at least %d numbers, got %d", idx+1, n+1, len(parts))
		}
		arr := make([]int, n)
		for i := 0; i < n; i++ {
			v, err := strconv.Atoi(parts[i+1])
			if err != nil {
				return nil, fmt.Errorf("line %d: bad int: %w", idx+1, err)
			}
			arr[i] = v
		}
		cases = append(cases, testCase{n: n, arr: arr})
	}
	return cases, nil
}

func runCase(bin string, tc testCase) error {
	var input strings.Builder
	input.WriteString(strconv.Itoa(tc.n))
	input.WriteByte('\n')
	for i, v := range tc.arr {
		if i > 0 {
			input.WriteByte(' ')
		}
		input.WriteString(strconv.Itoa(v))
	}
	input.WriteByte('\n')

	var cmd *exec.Cmd
	if strings.HasSuffix(bin, ".go") {
		cmd = exec.Command("go", "run", bin)
	} else {
		cmd = exec.Command(bin)
	}
	cmd.Stdin = strings.NewReader(input.String())
	var out bytes.Buffer
	var errBuf bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errBuf
	if err := cmd.Run(); err != nil {
		return fmt.Errorf("runtime error: %v\n%s", err, errBuf.String())
	}
	got := strings.TrimSpace(out.String())
	expected := strconv.Itoa(solve(tc.arr))
	if got != expected {
		return fmt.Errorf("expected %s got %s", expected, got)
	}
	return nil
}

