const ll inf = 3e18;
const ll Mod = 998244353;
const int N = 1e6 + 32;

inline ll MOD(ll a, ll mod=Mod) {a%=mod; (a<0)&&(a+=mod); return a;}
inline ll poww(ll a, ll b, ll mod=Mod) {
    ll ans = 1;
    a=MOD(a, mod);
    while (b>0) {
        if (b%2 == 1) ans = MOD(ans*a, mod);
        b >>= 1;
        a = MOD(a*a, mod);
    }
    return ans;
}

int t, n, ptr2, mark[N], a[N], used[N];
int evis[2*N], ptr[N];
vector<int> pr, vec;
vector<pii> adj[N];

void tour(int v) {
    if(ptr2 == n) return;
    while(ptr[v] < size(adj[v])) {
        pii u = adj[v][ptr[v]];
        if(evis[u.S]) {ptr[v]++; continue;}
        evis[u.S] = 1;
        tour(u.F);
        if(ptr2 == n) return;
        ptr[v]++;
    }
    if(used[v] == 0) {
        used[v] = 1;
        if(ptr2<n) a[++ptr2] = vec[v-1];
    }
    if(ptr2 == n) return;
    if(ptr2<n) a[++ptr2] = vec[v-1];
}

int main () {
	ios_base::sync_with_stdio(false), cin.tie(0);

    mark[1] = 1;
    for(int i=2; i<N; i++) {
        if(mark[i] == 1) continue;
        pr.pb(i);
        for(int j=2*i; j<N; j+=i) mark[j] = 1;
    }

	cin>>t;
    while(t--) {
        cin>>n;
        if(n==2) {
            cout<<"2 2\n"; continue;
        }
        for(int i=0; i<size(pr); i++) {
            vec.pb(pr[i]);
            if(size(vec)%2 == 1) {
                if(size(vec)*(size(vec)+1)/2 >= n-1) break;
            } else {
if(size(vec)*(size(vec)+1)/2-size(vec)/2+1 >= n-1) break;
            }
        }
        int nume = 0;
        if(size(vec)%2 == 1) {
            for(int i=1; i<=size(vec); i++) {
                for(int j=i+1; j<=size(vec); j++) {
                    nume++;
                    adj[i].pb({j, nume});
                    adj[j].pb({i, nume});
                }
            }
        } else {
            for(int i=1; i<=size(vec); i++) {
                for(int j=i+1; j<=size(vec); j++) {
                    if(i%2 == 1 && i+1 == j) continue;
                    nume++;
                    adj[i].pb({j, nume});
                    adj[j].pb({i, nume});
                }
            }
            nume++;
            adj[1].pb({2, nume});
            adj[2].pb({1, nume});
        }
        tour(1);
        for(int i=1; i<=n; i++) {
            cout<<a[i]<<' ';
        }
        cout<<endl;
        
        ptr2=0; vec.clear();
        for(int i=0; i<=n; i++) {
            adj[i].clear();
            a[i] = used[i] = evis[i] = ptr[i] = 0;
        }
        for(int i=1; i<=nume; i++) evis[i] = 0;
    }

	return 0;
}