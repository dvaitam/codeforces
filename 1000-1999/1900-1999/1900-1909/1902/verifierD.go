type query struct {
	x int
	y int
	l int
	r int
}

type testCase struct {
	input    string
	expected string
}

func key(x, y int) int64 {
	return int64(x+offset)*base + int64(y+offset)
}

func visitedOutside(arr []int, l, r int) bool {
	if len(arr) == 0 {
		return false
	}
	i := sort.SearchInts(arr, l)
	if i > 0 {
		return true
	}
	j := sort.SearchInts(arr, r)
	return j < len(arr)
}

func visitedInRange(arr []int, L, R int) bool {
	if len(arr) == 0 {
		return false
	}
	i := sort.SearchInts(arr, L)
	return i < len(arr) && arr[i] <= R
}

func solveCase(s string, qs []query) string {
	n := len(s)
	preX := make([]int, n+1)
	preY := make([]int, n+1)
	posMap := make(map[int64][]int)
	posMap[key(0, 0)] = []int{0}

	for i := 1; i <= n; i++ {
		preX[i] = preX[i-1]
		preY[i] = preY[i-1]
		switch s[i-1] {
		case 'U':
			preY[i]++
		case 'D':
			preY[i]--
		case 'L':
			preX[i]--
		case 'R':
			preX[i]++
		}
		k := key(preX[i], preY[i])
		posMap[k] = append(posMap[k], i)
	}

	var sb strings.Builder
	for idx, q := range qs {
		arr := posMap[key(q.x, q.y)]
		if visitedOutside(arr, q.l, q.r) {
			sb.WriteString("YES")
		} else {
			tx := preX[q.l-1] + preX[q.r] - q.x
			ty := preY[q.l-1] + preY[q.r] - q.y
			arr2 := posMap[key(tx, ty)]
			if visitedInRange(arr2, q.l-1, q.r-1) {
				sb.WriteString("YES")
			} else {
				sb.WriteString("NO")
			}
		}
		if idx+1 < len(qs) {
			sb.WriteByte('\n')
		}
	}
	return sb.String()
}

func parseCase(lines []string) (testCase, error) {
	if len(lines) < 2 {
		return testCase{}, fmt.Errorf("not enough lines in case")
	}
	header := strings.Fields(lines[0])
	if len(header) != 2 {
		return testCase{}, fmt.Errorf("bad header: %q", lines[0])
	}
	n, err := strconv.Atoi(header[0])
	if err != nil {
		return testCase{}, fmt.Errorf("bad n: %w", err)
	}
	q, err := strconv.Atoi(header[1])
	if err != nil {
		return testCase{}, fmt.Errorf("bad q: %w", err)
	}
	if len(lines) != 2+q {
		return testCase{}, fmt.Errorf("expected %d lines, got %d", 2+q, len(lines))
	}
	s := strings.TrimSpace(lines[1])
	if len(s) != n {
		return testCase{}, fmt.Errorf("string length %d does not match n %d", len(s), n)
	}
	qs := make([]query, q)
	var sb strings.Builder
	sb.WriteString(lines[0])
	sb.WriteByte('\n')
	sb.WriteString(s)
	sb.WriteByte('\n')
	for i := 0; i < q; i++ {
		fields := strings.Fields(lines[2+i])
		if len(fields) != 4 {
			return testCase{}, fmt.Errorf("bad query line: %q", lines[2+i])
		}
		x, err := strconv.Atoi(fields[0])
		if err != nil {
			return testCase{}, fmt.Errorf("bad x: %w", err)
		}
		y, err := strconv.Atoi(fields[1])
		if err != nil {
			return testCase{}, fmt.Errorf("bad y: %w", err)
		}
		l, err := strconv.Atoi(fields[2])
		if err != nil {
			return testCase{}, fmt.Errorf("bad l: %w", err)
		}
		r, err := strconv.Atoi(fields[3])
		if err != nil {
			return testCase{}, fmt.Errorf("bad r: %w", err)
		}
		qs[i] = query{x: x, y: y, l: l, r: r}
		sb.WriteString(lines[2+i])
		sb.WriteByte('\n')
	}

	expected := solveCase(s, qs)
	return testCase{input: sb.String(), expected: expected}, nil
}

func loadCases() ([]testCase, error) {
	lines := strings.Split(testcasesD, "\n")
	var cur []string
	var cases []testCase
	firstLineSkipped := false
	addCase := func() error {
		if len(cur) == 0 {
			return nil
		}
		tc, err := parseCase(cur)
		if err != nil {
			return err
		}
		cases = append(cases, tc)
		cur = cur[:0]
		return nil
	}
	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if trimmed == "" {
			if err := addCase(); err != nil {
				return nil, err
			}
			continue
		}
		if !firstLineSkipped {
			if len(strings.Fields(trimmed)) == 1 {
				firstLineSkipped = true
				continue
			}
			firstLineSkipped = true
		}
		cur = append(cur, line)
	}
	if err := addCase(); err != nil {
		return nil, err
	}
	return cases, nil
}

func runCandidate(bin, input string) (string, error) {
	cmd := exec.Command(bin)
	cmd.Stdin = strings.NewReader(input)
	out, err := cmd.CombinedOutput()
	if err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, string(out))
	}
	return strings.TrimSpace(string(out)), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: verifierD /path/to/binary")
		os.Exit(1)
	}
	cases, err := loadCases()
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to load testcases: %v\n", err)
		os.Exit(1)
	}
	for idx, tc := range cases {
		got, err := runCandidate(os.Args[1], tc.input)
		if err != nil {
			fmt.Printf("case %d: %v\n", idx+1, err)
			os.Exit(1)
		}
		if got != tc.expected {
			fmt.Printf("case %d failed\nexpected: %s\ngot: %s\n", idx+1, tc.expected, got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(cases))
}
