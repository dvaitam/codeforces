func canCover(grid [][]byte) bool {
	n := len(grid)
	for i := 1; i < n-1; i++ {
		for j := 1; j < n-1; j++ {
			if grid[i][j] == '#' &&
				grid[i-1][j] == '#' &&
				grid[i+1][j] == '#' &&
				grid[i][j-1] == '#' &&
				grid[i][j+1] == '#' {
				grid[i][j] = '.'
				grid[i-1][j] = '.'
				grid[i+1][j] = '.'
				grid[i][j-1] = '.'
				grid[i][j+1] = '.'
			}
		}
	}
	for i := 0; i < n; i++ {
		for j := 0; j < n; j++ {
			if grid[i][j] == '#' {
				return false
			}
		}
	}
	return true
}

func runCandidate(bin, input string) (string, error) {
	var cmd *exec.Cmd
	if strings.HasSuffix(bin, ".go") {
		cmd = exec.Command("go", "run", bin)
	} else {
		cmd = exec.Command(bin)
	}
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var errBuf bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errBuf
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, errBuf.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierB.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]
	lines := strings.Split(strings.TrimSpace(embeddedTestcases), "\n")
	for idx, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		fields := strings.Fields(line)
		if len(fields) < 1 {
			fmt.Fprintf(os.Stderr, "test %d: empty\n", idx+1)
			os.Exit(1)
		}
		n, err := strconv.Atoi(fields[0])
		if err != nil {
			fmt.Fprintf(os.Stderr, "test %d: bad n\n", idx+1)
			os.Exit(1)
		}
		if len(fields) != 1+n {
			fmt.Fprintf(os.Stderr, "test %d: expected %d rows got %d\n", idx+1, n, len(fields)-1)
			os.Exit(1)
		}
		grid := make([][]byte, n)
		for i := 0; i < n; i++ {
			grid[i] = []byte(fields[1+i])
		}
		expected := "NO"
		if canCover(cloneGrid(grid)) {
			expected = "YES"
		}
		var input strings.Builder
		input.WriteString(fmt.Sprintf("%d\n", n))
		for i := 0; i < n; i++ {
			input.WriteString(fields[1+i])
			input.WriteByte('\n')
		}
		got, err := runCandidate(bin, input.String())
		if err != nil {
			fmt.Printf("test %d failed: %v\n", idx+1, err)
			os.Exit(1)
		}
		if got != expected {
			fmt.Printf("test %d failed\nexpected: %s\ngot: %s\n", idx+1, expected, got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(lines))
}

func cloneGrid(g [][]byte) [][]byte {
	res := make([][]byte, len(g))
	for i := range g {
		res[i] = make([]byte, len(g[i]))
		copy(res[i], g[i])
	}
	return res
}
