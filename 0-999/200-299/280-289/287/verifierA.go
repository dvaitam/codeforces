type testCase [4]string

// solveCase mirrors 287A.go.
func solveCase(grid testCase) string {
	for i := 0; i < 3; i++ {
		for j := 0; j < 3; j++ {
			cnt := 0
			if grid[i][j] == '#' {
				cnt++
			}
			if grid[i][j+1] == '#' {
				cnt++
			}
			if grid[i+1][j] == '#' {
				cnt++
			}
			if grid[i+1][j+1] == '#' {
				cnt++
			}
			if cnt != 2 {
				return "YES"
			}
		}
	}
	return "NO"
}

func loadTestcases() ([]testCase, error) {
	lines := strings.Split(testcasesRaw, "\n")
	var cases []testCase
	var cur []string
	for idx, line := range lines {
		trim := strings.TrimSpace(line)
		if trim == "" {
			if len(cur) == 4 {
				var tc testCase
				copy(tc[:], cur)
				cases = append(cases, tc)
			} else if len(cur) != 0 {
				return nil, fmt.Errorf("incomplete case ending at line %d", idx+1)
			}
			cur = nil
			continue
		}
		cur = append(cur, trim)
	}
	if len(cur) == 4 {
		var tc testCase
		copy(tc[:], cur)
		cases = append(cases, tc)
	} else if len(cur) != 0 {
		return nil, fmt.Errorf("incomplete trailing case")
	}
	return cases, nil
}

func run(bin, input string) (string, error) {
	cmd := exec.Command(bin)
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var stderr bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &stderr
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, stderr.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]
	testcases, err := loadTestcases()
	if err != nil {
		fmt.Fprintf(os.Stderr, "failed to load testcases: %v\n", err)
		os.Exit(1)
	}
	for idx, tc := range testcases {
		expect := solveCase(tc)
		input := strings.Join(tc[:], "\n") + "\n"
		got, err := run(bin, input)
		if err != nil {
			fmt.Fprintf(os.Stderr, "case %d failed: %v\n", idx+1, err)
			os.Exit(1)
		}
		if strings.TrimSpace(got) != expect {
			fmt.Fprintf(os.Stderr, "case %d failed: expected %s got %s\nInput:\n%s\n", idx+1, expect, got, input)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(testcases))
}
