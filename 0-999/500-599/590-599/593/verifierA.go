func solve(words []string) int {
	type word struct {
		mask uint32
		len  int
	}
	arr := make([]word, 0, len(words))
	for _, w := range words {
		var m uint32
		for _, ch := range w {
			m |= 1 << (ch - 'a')
		}
		if bits.OnesCount32(m) <= 2 {
			arr = append(arr, word{m, len(w)})
		}
	}
	best := 0
	for i := 0; i < 26; i++ {
		for j := i; j < 26; j++ {
			mask := uint32(1<<i | 1<<j)
			sum := 0
			for _, w := range arr {
				if w.mask&^mask == 0 {
					sum += w.len
				}
			}
			if sum > best {
				best = sum
			}
		}
	}
	return best
}

