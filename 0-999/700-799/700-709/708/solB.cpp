int main() {
    optimize();

    int a00, a11, a10, a01, zero = -1, one = -1;
    cin >> a00 >> a01 >> a10 >> a11;

    for(ll i = 1; (i*(i-1))/2 <= a00 || (i*(i-1))/2 <= a11; ++i) {
            if((i*(i-1))/2 == a00) zero = i;
            if((i*(i-1))/2 == a11) one = i;
    }

    if(zero == -1 || one == -1) NO();

    if(!a00 && !a11 && !a01 && !a10) {
            cout << 0;
            return 0;
    }

    if(!a00 && !a01 && !a10) {
            while(one--) cout << 1;
            return 0;
    }

    if(!a11 && !a01 && !a10) {
            while(zero--) cout << 0;
            return 0;
    }

    if((ll)zero * (ll)one != a10 + a01) NO();

    int b01 = one * zero, lol = 0;

    while(b01 - a01 >= one) {
            ++lol;
            b01 -= one;
    }

    dbg(lol, b01, a01);
    int extra = b01 - a01;

    for(int i = 0; i < zero - lol - ((extra > 0)?1:0); ++i) cout << 0;
    for(int i = 0; i < extra; ++i) cout << 1;
    if(extra) cout << 0;
    for(int i = 0; i < one - extra; ++i) cout << 1;
    for(int i = 0; i < lol; ++i) cout << 0;

    return 0;
}