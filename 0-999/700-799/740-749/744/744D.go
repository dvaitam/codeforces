package main

import (
   "bufio"
   "fmt"
   "os"
)

func main() {
   reader := bufio.NewReader(os.Stdin)
   writer := bufio.NewWriter(os.Stdout)
   defer writer.Flush()

   var n, m int
   if _, err := fmt.Fscan(reader, &n, &m); err != nil {
       return
   }
   // TODO: Implement conversion of solD.cpp solution in Go
   // For now, prints -1 as placeholder
   fmt.Fprint(writer, -1)
}
