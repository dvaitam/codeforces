func solve673A(times []int) int {
	last := 0
	for _, t := range times {
		if t-last > 15 {
			return last + 15
		}
		last = t
	}
	if last+15 <= 90 {
		return last + 15
	}
	return 90
}

func runCandidate(bin, input string) (string, error) {
	cmd := exec.Command(bin)
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return "", err
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]
	scanner := bufio.NewScanner(strings.NewReader(embeddedTestcasesA))
	scanner.Split(bufio.ScanWords)
	// increase buffer for large inputs
	scanner.Buffer(make([]byte, 1024), 1<<20)
	nextInt := func() int {
		if !scanner.Scan() {
			fmt.Fprintln(os.Stderr, "unexpected EOF in test data")
			os.Exit(1)
		}
		v, err := strconv.Atoi(scanner.Text())
		if err != nil {
			fmt.Fprintf(os.Stderr, "parse error: %v\n", err)
			os.Exit(1)
		}
		return v
	}

	t := nextInt()
	for caseIdx := 1; caseIdx <= t; caseIdx++ {
		n := nextInt()
		times := make([]int, n)
		for i := 0; i < n; i++ {
			times[i] = nextInt()
		}
		want := strconv.Itoa(solve673A(times))

		var input strings.Builder
		fmt.Fprintf(&input, "%d\n", n)
		for i, v := range times {
			if i > 0 {
				input.WriteByte(' ')
			}
			input.WriteString(strconv.Itoa(v))
		}
		input.WriteByte('\n')

		got, err := runCandidate(bin, input.String())
		if err != nil {
			fmt.Fprintf(os.Stderr, "case %d failed: %v\n", caseIdx, err)
			os.Exit(1)
		}
		if got != want {
			fmt.Fprintf(os.Stderr, "case %d failed: expected %s got %s\n", caseIdx, want, got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", t)
}
