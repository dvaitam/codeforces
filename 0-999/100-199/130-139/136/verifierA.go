type testCase struct {
	n   int
	arr []int
}

// Embedded reference logic from 136A.go.
func solve(tc testCase) []int {
	ans := make([]int, tc.n)
	for i, v := range tc.arr {
		if v >= 1 && v <= tc.n {
			ans[v-1] = i + 1
		}
	}
	return ans
}

func parseTestcases(raw string) ([]testCase, error) {
	sc := bufio.NewScanner(strings.NewReader(raw))
	sc.Split(bufio.ScanWords)
	if !sc.Scan() {
		return nil, fmt.Errorf("empty test data")
	}
	t, err := strconv.Atoi(sc.Text())
	if err != nil {
		return nil, fmt.Errorf("invalid test count")
	}
	tests := make([]testCase, 0, t)
	for i := 0; i < t; i++ {
		if !sc.Scan() {
			return nil, fmt.Errorf("missing n for case %d", i+1)
		}
		n, err := strconv.Atoi(sc.Text())
		if err != nil {
			return nil, fmt.Errorf("invalid n for case %d", i+1)
		}
		arr := make([]int, n)
		for j := 0; j < n; j++ {
			if !sc.Scan() {
				return nil, fmt.Errorf("missing value %d for case %d", j+1, i+1)
			}
			v, err := strconv.Atoi(sc.Text())
			if err != nil {
				return nil, fmt.Errorf("invalid value %d for case %d", j+1, i+1)
			}
			arr[j] = v
		}
		tests = append(tests, testCase{n: n, arr: arr})
	}
	return tests, nil
}

func run(bin, input string) (string, error) {
	var cmd *exec.Cmd
	if strings.HasSuffix(bin, ".go") {
		cmd = exec.Command("go", "run", bin)
	} else {
		cmd = exec.Command(bin)
	}
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var errBuf bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errBuf
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, errBuf.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierA.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]

	tests, err := parseTestcases(testcasesRaw)
	if err != nil {
		fmt.Fprintln(os.Stderr, "failed to parse tests:", err)
		os.Exit(1)
	}

	for idx, tc := range tests {
		var input strings.Builder
		input.WriteString(fmt.Sprintf("%d\n", tc.n))
		for i, v := range tc.arr {
			if i > 0 {
				input.WriteByte(' ')
			}
			input.WriteString(strconv.Itoa(v))
		}
		input.WriteByte('\n')

		expected := solve(tc)
		var expectStr strings.Builder
		for i, v := range expected {
			if i > 0 {
				expectStr.WriteByte(' ')
			}
			expectStr.WriteString(strconv.Itoa(v))
		}

		got, err := run(bin, input.String())
		if err != nil {
			fmt.Fprintf(os.Stderr, "case %d failed: %v\n", idx+1, err)
			os.Exit(1)
		}
		if strings.TrimSpace(got) != expectStr.String() {
			fmt.Fprintf(os.Stderr, "case %d mismatch: expected %s got %s\n", idx+1, expectStr.String(), strings.TrimSpace(got))
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(tests))
}
