// reference solver based on 125D implementation
func referenceSolve(arr []int) string {
	n := len(arr)
	var buf bytes.Buffer

	printSeq := func(seq []int) {
		for i, v := range seq {
			if i > 0 {
				buf.WriteByte(' ')
			}
			buf.WriteString(strconv.Itoa(v))
		}
		buf.WriteByte('\n')
	}

	check := func(seq []int) bool {
		if len(seq) == 0 {
			return false
		}
		if len(seq) <= 2 {
			return true
		}
		d := seq[1] - seq[0]
		for i := 2; i < len(seq); i++ {
			if seq[i]-seq[i-1] != d {
				return false
			}
		}
		return true
	}

	attempt := func(l, r int) bool {
		tk := make([]bool, n)
		seq1 := make([]int, 0, n)
		seq2 := make([]int, 0, n)

		nd := arr[l]
		delta := arr[r] - arr[l]
		lastIdx := -1
		for i, v := range arr {
			if v == nd {
				seq1 = append(seq1, v)
				tk[i] = true
				lastIdx = i
				nd += delta
			}
		}

		for i, v := range arr {
			if !tk[i] {
				seq2 = append(seq2, v)
			}
		}
		if check(seq2) {
			printSeq(seq1)
			printSeq(seq2)
			return true
		}

		if lastIdx >= 0 {
			tk[lastIdx] = false
			seq1 = seq1[:len(seq1)-1]
		}
		seq2 = seq2[:0]
		for i, v := range arr {
			if !tk[i] {
				seq2 = append(seq2, v)
			}
		}
		if check(seq2) {
			printSeq(seq1)
			printSeq(seq2)
			return true
		}
		return false
	}

	if n == 2 {
		printSeq([]int{arr[0]})
		printSeq([]int{arr[1]})
		return strings.TrimSpace(buf.String())
	}

	if attempt(0, 1) || attempt(0, 2) || attempt(1, 2) {
		return strings.TrimSpace(buf.String())
	}
	buf.Reset()
	buf.WriteString("No solution")
	return buf.String()
}

func parseTestcases() ([][]int, error) {
	scan := bufio.NewScanner(strings.NewReader(testcases))
	scan.Split(bufio.ScanWords)
	if !scan.Scan() {
		return nil, fmt.Errorf("empty testcases")
	}
	t, err := strconv.Atoi(scan.Text())
	if err != nil {
		return nil, fmt.Errorf("parse t: %w", err)
	}
	cases := make([][]int, t)
	for i := 0; i < t; i++ {
		if !scan.Scan() {
			return nil, fmt.Errorf("missing n for case %d", i+1)
		}
		n, err := strconv.Atoi(scan.Text())
		if err != nil {
			return nil, fmt.Errorf("parse n case %d: %w", i+1, err)
		}
		arr := make([]int, n)
		for j := 0; j < n; j++ {
			if !scan.Scan() {
				return nil, fmt.Errorf("missing value %d case %d", j+1, i+1)
			}
			v, err := strconv.Atoi(scan.Text())
			if err != nil {
				return nil, fmt.Errorf("parse value %d case %d: %w", j+1, i+1, err)
			}
			arr[j] = v
		}
		cases[i] = arr
	}
	if err := scan.Err(); err != nil {
		return nil, fmt.Errorf("scanner error: %w", err)
	}
	return cases, nil
}

func run(bin, input string) (string, error) {
	cmd := exec.Command(bin)
	cmd.Stdin = strings.NewReader(input)
	var out bytes.Buffer
	var errb bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &errb
	if err := cmd.Run(); err != nil {
		return "", fmt.Errorf("runtime error: %v\n%s", err, errb.String())
	}
	return strings.TrimSpace(out.String()), nil
}

func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: go run verifierD.go /path/to/binary")
		os.Exit(1)
	}
	bin := os.Args[1]

	cases, err := parseTestcases()
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}

	for idx, arr := range cases {
		var sb strings.Builder
		sb.WriteString(fmt.Sprintf("%d\n", len(arr)))
		for i, v := range arr {
			if i > 0 {
				sb.WriteByte(' ')
			}
			sb.WriteString(strconv.Itoa(v))
		}
		sb.WriteByte('\n')
		input := sb.String()

		want := referenceSolve(arr)
		got, err := run(bin, input)
		if err != nil {
			fmt.Printf("case %d failed: %v\n", idx+1, err)
			os.Exit(1)
		}
		if strings.TrimSpace(got) != strings.TrimSpace(want) {
			fmt.Printf("case %d failed\ninput:\n%sexpected:\n%s\ngot:\n%s\n", idx+1, input, want, got)
			os.Exit(1)
		}
	}
	fmt.Printf("All %d tests passed\n", len(cases))
}
